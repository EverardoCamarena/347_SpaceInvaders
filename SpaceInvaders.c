void System_Init(void){
  DisableInterrupts();
  PLL_Init(Bus80MHz);        // set system clock to 80 MHz
  SysTick_Init(8000000);     // Load for 0.1 sec/10Hz
	Switch_Init();
  ADC0_InitSWTriggerSeq3_Ch1();
  Nokia5110_Init();
  Nokia5110_ClearBuffer();
	Nokia5110_DisplayBuffer(); // draw buffer
	Sound_Init();
  EnableInterrupts();
}

// Display the game start prompt
void Start_Prompt(void){
	uint8_t start_prompt[]="   Space      Invader    Press SW2   To Start!";
	Nokia5110_Clear();
  Nokia5110_OutString((char *)start_prompt);
}

// Display the game end prompt
void End_Prompt(void){
	uint8_t end_prompt[]=" Game Over   Nice Try!  Your Score:  ";
	uint8_t start_prompt[]="   Space      Invader    Press SW2   To Start!";
	
	// display the end prompt for 3 seconds
	Nokia5110_Clear();
  Nokia5110_OutString((char *)end_prompt);
	Nokia5110_OutUDec(score);
	for(int i = 0; i < 30; i++){ // wait 30 iterations (30 * 0.1) = 3sec
		while(!time_to_draw){}     // wait for systick handler flag
		time_to_draw = 0;
	}
	
	// display the start prompt after 3 seconds
	Nokia5110_Clear();
	Nokia5110_OutString((char *)start_prompt);
}

// Initialize the game: initialize all sprites and 
// reset refresh control and game status.
void Game_Init(void){
  time_to_draw = 0;
  score = 0; // reset score
	uint8_t i;
	
	// Version 2: add enemy initialization with close posture.
	if(currentLevel == 1){
		// Initialize 3 enemies for level 1
		for(i = 0; i < 3; i++){
				Enemy[i].x = 20 * i;
				Enemy[i].y = 10;
				Enemy[i].image = SmallEnemyPointA[i];
				Enemy[i].life = ALIVE;
		}
  }else if(currentLevel == 2){
		// Initialize 9 enemies for level 2
		for(i = 0; i < 9; i++) {
				Enemy[i].x = (i % 3) * 20;
				Enemy[i].y = 10 + (i / 3) * 10;
				Enemy[i].image = SmallEnemyPointA[i % 3];
				Enemy[i].life = ALIVE;
		}
  }

  // Version 3: add player ship initialization
  PlayerShip.x = MAX_X/2;
	PlayerShip.y = MAX_Y - 1;
	PlayerShip.image = PlayerShip0;
	PlayerShip.life = ALIVE;
	
  // Version 4: Add bullet initialization: you can choose Laser or Missile
	Bullet.x = PlayerShip.x;
	Bullet.y = PlayerShip.y + 8;
	Bullet.image = Laser0;
	Bullet.life = DEAD;
	
	SmallExplosion.x = Bullet.x;
	SmallExplosion.y = Bullet.y;
	SmallExplosion.life = DEAD;
}

// Update positions for all alive sprites.
void Move(void){
  uint8_t num_life = 0;
	uint8_t i;
	unsigned long ADCvalue;
  unsigned int playershipposition;
	uint8_t enemySpeed = (currentLevel == 1) ? 1 : 2;
	uint8_t level2_prompt[]=" Get Ready      for      Level 2!!! ";
	
  // V2: Move enemies, check life or dead: dead if right side reaches right screen border or detect a hit   
	for(i=0;i<9;i++){
		if(Enemy[i].x < MAX_ENEMYX){
			num_life += 1;
			Enemy[i].x += enemySpeed;
			if(Enemy[i].image == SmallEnemyPointA[i%3]){
				Enemy[i].image = SmallEnemyPointB[i%3];
		  }
			else{
				Enemy[i].image = SmallEnemyPointA[i%3];
			}
		}
		else{
				Enemy[i].life = DEAD;
			}
		}
	
	// Read ADC and update player ship position: only x coordinate will be changed. 
	ADCvalue = ADC0_InSeq3();
	playershipposition = ADCvalue*(SCREENW-18)/4095;
	PlayerShip.x = playershipposition;
	
	if(sw1Pressed){
		Bullet.x = PlayerShip.x + 8;
		Bullet.y = PlayerShip.y - 7;
		Bullet.life = ALIVE;
		sw1Pressed = 0;
	}
	
	// take care of collision with the enemy
	for(i=0;i<9;i++){
		if (Enemy[i].life == ALIVE && Bullet.life == ALIVE){
			if(((Bullet.y - BULLETH <= Enemy[i].y) && Bullet.x <= Enemy[i].x + ENEMY_WIDTH) && (Bullet.x >= Enemy[i].x) && (Bullet.x + BULLETW >= Enemy[i].x) && (Bullet.x + BULLETW <= Enemy[i].x + ENEMY_WIDTH)){
					Enemy[i].life = DEAD;
					SmallExplosion.x = Enemy[i].x;
					SmallExplosion.y = Enemy[i].y;
					SmallExplosion.life = ALIVE;
					Bullet.life = DEAD;
					score++;
			}
		}
	}
	
	if(Bullet.life){
		if(Bullet.y>2){
			Bullet.y-=2;
		}else{
			Bullet.life = DEAD;
		}
	}
	
	if(SmallExplosion.life){
		counter++;
		SmallExplosion.image = SmallExplosion0;
		Sound_Explosion();
		if(counter==3){
			SmallExplosion.life = DEAD;
			counter = 0;
		}
	}
	
  // Check for level completion
  if(num_life == 0){
		if(currentLevel == 1 && score == 3){
			currentLevel = 2;
			Nokia5110_Clear();
			Nokia5110_OutString((char *)level2_prompt);
			for(int i = 0; i < 20; i++){ // wait 20 iterations (20 * 0.1) = 2sec
				while(!time_to_draw){}     // wait for systick handler flag
				time_to_draw = 0;
        }
			
			Game_Init();                 // Initialize level 2
			}else{
				game_s = OVER;             // End the game if level 2 is completed
			}
	}
}

// Update the screen: 
// clear display and update the screen with the 
// current positions of all sprites that are alive.
void Draw(void){
	// enemy start with close posture: SmallEnemyPointA
  static uint8_t enemy_posture = CLOSE;
  uint8_t i;
  
  if (game_s==OVER) return;
  
  Nokia5110_ClearBuffer();
  
  // V2: update live enemies' positions in display buffer
  for(i=0;i<9;i++){
    if(Enemy[i].life > DEAD){
			Nokia5110_PrintBMP(Enemy[i].x, Enemy[i].y, Enemy[i].image, 0);
    }
  }
  
  // V3: Update the player ship position in display buffer
	Nokia5110_PrintBMP(PlayerShip.x, PlayerShip.y, PlayerShip.image, 0);
	
  // V4: Update the bullet position in display buffer if there is one.
  if (Bullet.life==ALIVE) {
		Nokia5110_PrintBMP(Bullet.x, Bullet.y, Bullet.image, 0);
  }

	// V4: Update the small explosion position in display buffer if there is one.
  if (SmallExplosion.life==ALIVE) {
		Nokia5110_PrintBMP(SmallExplosion.x, SmallExplosion.y, SmallExplosion.image, 0);
  }
	
	// Update the display with information in display buffer
  Nokia5110_DisplayBuffer();
}

// Handler for SysTick to control screen refresh rate @ 10Hz. 
void SysTick_Handler(void){
  time_to_draw = 1;
}

// Handler for port F
void GPIOPortF_Handler(void){    // called on release of either SW1 or SW2
	// take care of button debounce
	for (uint32_t time=0;time<72724;time++) {}
		
	// SW1: shoot a bullet upon switch press
	if(GPIO_PORTF_RIS_R & SW1){  // SW1 touched
    GPIO_PORTF_ICR_R = SW1;    // acknowledge flag0
		sw1Pressed = 1;
		Sound_Shoot();
  }
	
	// SW2: start the game, change the game status to ON
  if(GPIO_PORTF_RIS_R & SW2){  // SW2 touched 
    GPIO_PORTF_ICR_R = SW2;    // acknowledge flag4
		game_s = ON;
  }
}
